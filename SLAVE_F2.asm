DELAY_1MS:
    MOVLW   0xC8
    MOVWF   DELAY_COUNT1
DELAY_1MS_LOOP:
    NOP
    NOP
    DECFSZ  DELAY_COUNT1, F
    GOTO    DELAY_1MS_LOOP
    RETURN

DELAY_5MS:
    MOVLW   0x05
    MOVWF   DELAY_COUNT2
DELAY_5MS_LOOP:
    CALL    DELAY_1MS
    DECFSZ  DELAY_COUNT2, F
    GOTO    DELAY_5MS_LOOP
    RETURN

DELAY_50MS:
    MOVLW   0x32
    MOVWF   DELAY_COUNT2
DELAY_50MS_LOOP:
    CALL    DELAY_1MS
    DECFSZ  DELAY_COUNT2, F
    GOTO    DELAY_50MS_LOOP
    RETURN
