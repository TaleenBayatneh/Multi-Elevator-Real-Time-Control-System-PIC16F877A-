    CLRF    E1_SENSORS
    CLRF    E1_FLOOR_SENSORS
    CLRF    E1_BUZZER_TIMER
    CLRF    E1_DOOR_TIMER
    MOVLW   STATE_IDLE
    MOVWF   E2_STATE
    MOVLW   FLOOR_1
    MOVWF   E2_CURRENT_FLOOR
    CLRF    E2_TARGET_FLOOR
    CLRF    E2_DIRECTION
    CLRF    E2_REQUESTS_UP
    CLRF    E2_REQUESTS_DN
    CLRF    E2_CABIN_REQUESTS
    CLRF    E2_SENSORS
    CLRF    E2_FLOOR_SENSORS
    CLRF    E2_BUZZER_TIMER
