SCAN_ALL_SENSORS:
    MOVLW   0x01
    MOVWF   CURRENT_ELEVATOR
    CALL    SCAN_ELEVATOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E1_SENSORS
    CALL    SCAN_FLOOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E1_FLOOR_SENSORS
    CALL    SCAN_CABIN_BUTTONS
    MOVF    SCAN_RESULT, W
    IORWF   E1_CABIN_REQUESTS, F
    MOVLW   0x02
    MOVWF   CURRENT_ELEVATOR
    CALL    SCAN_ELEVATOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E2_SENSORS
    CALL    SCAN_FLOOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E2_FLOOR_SENSORS
    CALL    SCAN_CABIN_BUTTONS
    MOVF    SCAN_RESULT, W
    IORWF   E2_CABIN_REQUESTS, F
    MOVLW   0x03
    MOVWF   CURRENT_ELEVATOR
    CALL    SCAN_ELEVATOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E3_SENSORS
    CALL    SCAN_FLOOR_SENSORS
    MOVF    SCAN_RESULT, W
    MOVWF   E3_FLOOR_SENSORS
    CALL    SCAN_CABIN_BUTTONS
    MOVF    SCAN_RESULT, W
    IORWF   E3_CABIN_REQUESTS, F
    RETURN

SELECT_MUX:
    BSF     PORTB, 4
    BSF     PORTB, 5
    BSF     PORTB, 6
    MOVF    CURRENT_ELEVATOR, W
    SUBLW   0x01
    BTFSC   STATUS, Z
    BCF     PORTB, 4
    MOVF    CURRENT_ELEVATOR, W
    SUBLW   0x02
    BTFSC   STATUS, Z
    BCF     PORTB, 5
    MOVF    CURRENT_ELEVATOR, W
    SUBLW   0x03
    BTFSC   STATUS, Z
    BCF     PORTB, 6
    RETURN

SET_MUX_CHANNEL:
    MOVF    MUX_CHANNEL, W
    ANDLW   0x0F
    MOVWF   TEMP_REG
    MOVF    PORTB, W
    ANDLW   0xF0
    IORWF   TEMP_REG, W
    MOVWF   PORTB
    CALL    DELAY_50US
    RETURN

READ_MUX_OUTPUT:
    MOVF    CURRENT_ELEVATOR, W
    SUBLW   0x01
    BTFSC   STATUS, Z
    GOTO    READ_RA0
    MOVF    CURRENT_ELEVATOR, W
    SUBLW   0x02
    BTFSC   STATUS, Z
    GOTO    READ_RA1
    GOTO    READ_RA2
READ_RA0:
    BTFSC   PORTA, 0
    RETLW   0x01
    RETLW   0x00
READ_RA1:
    BTFSC   PORTA, 1
    RETLW   0x01
    RETLW   0x00
READ_RA2:
    BTFSC   PORTA, 2
    RETLW   0x01
    RETLW   0x00

SCAN_ELEVATOR_SENSORS:
    CALL    SELECT_MUX
    CLRF    SCAN_RESULT
    MOVLW   CH_DOOR_CLOSED
    MOVWF   MUX_CHANNEL
    CALL    SET_MUX_CHANNEL
    CALL    READ_MUX_OUTPUT
    BTFSS   STATUS, Z
    BSF     SCAN_RESULT, 0
    MOVLW   CH_DOOR_OPEN
    MOVWF   MUX_CHANNEL
    CALL    SET_MUX_CHANNEL
    CALL    READ_MUX_OUTPUT
    BTFSS   STATUS, Z
    BSF     SCAN_RESULT, 1
    MOVLW   CH_IR_SENSOR
    MOVWF   MUX_CHANNEL
    CALL    SET_MUX_CHANNEL
    CALL    READ_MUX_OUTPUT
    BTFSS   STATUS, Z
    BSF     SCAN_RESULT, 2
    MOVLW   CH_OVERWEIGHT
    MOVWF   MUX_CHANNEL
    CALL    SET_MUX_CHANNEL
    CALL    READ_MUX_OUTPUT
    BTFSS   STATUS, Z
    BSF     SCAN_RESULT, 3
    RETURN

