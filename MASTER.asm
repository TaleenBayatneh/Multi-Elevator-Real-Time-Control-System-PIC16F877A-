    MOVLW   STATE_IDLE
    MOVWF   E1_STATE
    MOVLW   FLOOR_GF
    MOVWF   E1_CURRENT_FLOOR
    CLRF    E1_TARGET_FLOOR
    CLRF    E1_DIRECTION
    CLRF    E1_REQUESTS_UP
    CLRF    E1_REQUESTS_DN
