UPDATE_ELEVATOR_1:
    BTFSC   E1_FLOOR_SENSORS, 0
    CLRF    E1_CURRENT_FLOOR
    BTFSC   E1_FLOOR_SENSORS, 1
    MOVLW   FLOOR_1
    BTFSC   E1_FLOOR_SENSORS, 1
    MOVWF   E1_CURRENT_FLOOR
    BTFSC   E1_FLOOR_SENSORS, 2
    MOVLW   FLOOR_2
    BTFSC   E1_FLOOR_SENSORS, 2
    MOVWF   E1_CURRENT_FLOOR
    MOVF    E1_STATE, W
    SUBLW   STATE_IDLE
    BTFSC   STATUS, Z
    GOTO    E1_IDLE_H
    MOVF    E1_STATE, W
    SUBLW   STATE_UP
    BTFSC   STATUS, Z
    GOTO    E1_UP_H
    MOVF    E1_STATE, W
    SUBLW   STATE_DOWN
    BTFSC   STATUS, Z
    GOTO    E1_DOWN_H
    RETURN

E1_IDLE_H:
    MOVF    E1_REQUESTS_UP, W
    IORWF   E1_REQUESTS_DN, W
    MOVWF   TEMP_REG
    MOVF    E1_CABIN_REQUESTS, W
    ANDLW   0x07
    IORWF   TEMP_REG, W
    BTFSC   STATUS, Z
    RETURN
    CALL    FIND_E1_TGT
    MOVF    E1_TARGET_FLOOR, W
    SUBWF   E1_CURRENT_FLOOR, W
    BTFSC   STATUS, Z
    GOTO    E1_AT_TGT
    BTFSS   STATUS, C
    GOTO    E1_GO_UP
    GOTO    E1_GO_DN

E1_GO_UP:
    MOVLW   STATE_UP
    MOVWF   E1_STATE
    MOVWF   E1_DIRECTION
    BSF     PORTD, 0
    BCF     PORTD, 1
    RETURN

E1_GO_DN:
    MOVLW   STATE_DOWN
    MOVWF   E1_STATE
    MOVWF   E1_DIRECTION
    BCF     PORTD, 0
    BSF     PORTD, 1
    RETURN

E1_AT_TGT:
    CALL    CLR_E1_REQ
    MOVF    E1_CURRENT_FLOOR, W
    CALL    CLR_FLR_SVC
    MOVLW   BUZZER_TIME
    MOVWF   E1_BUZZER_TIMER
    BSF     PORTD, 4
    MOVLW   DELAY_2SEC
    MOVWF   E1_DOOR_TIMER
    MOVLW   STATE_DOOR_OPEN
    MOVWF   E1_STATE
    RETURN

E1_UP_H:
    MOVF    E1_FLOOR_SENSORS, W
    BTFSC   STATUS, Z
    RETURN
    BTFSS   E1_FLOOR_SENSORS, 0
    GOTO    E1_UP_C1
    BTFSC   E1_REQUESTS_UP, 0
    GOTO    E1_STOP
    BTFSC   E1_CABIN_REQUESTS, 0
    GOTO    E1_STOP
E1_UP_C1:
    BTFSS   E1_FLOOR_SENSORS, 1
    GOTO    E1_UP_C2
    BTFSC   E1_REQUESTS_UP, 1
    GOTO    E1_STOP
    BTFSC   E1_CABIN_REQUESTS, 1
    GOTO    E1_STOP
E1_UP_C2:
    BTFSS   E1_FLOOR_SENSORS, 2
    RETURN
    BTFSC   E1_CABIN_REQUESTS, 2
    GOTO    E1_STOP
    BTFSC   E1_REQUESTS_UP, 2
    GOTO    E1_STOP
    MOVF    E1_REQUESTS_DN, W
    BTFSS   STATUS, Z
    GOTO    E1_REV_DN
    GOTO    E1_STOP

E1_STOP:
    BCF     PORTD, 0
    BCF     PORTD, 1
    CALL    CLR_E1_REQ
    MOVF    E1_CURRENT_FLOOR, W
    CALL    CLR_FLR_SVC
    MOVLW   BUZZER_TIME
    MOVWF   E1_BUZZER_TIMER
    BSF     PORTD, 4
    MOVLW   DELAY_2SEC
    MOVWF   E1_DOOR_TIMER
    MOVLW   STATE_DOOR_OPEN
    MOVWF   E1_STATE
    RETURN

E1_REV_DN:
    MOVLW   STATE_DOWN
    MOVWF   E1_STATE
    MOVWF   E1_DIRECTION
    BCF     PORTD, 0
    BSF     PORTD, 1
