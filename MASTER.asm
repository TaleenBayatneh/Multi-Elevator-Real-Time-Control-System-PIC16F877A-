I2C_START_COND:
    BSF     STATUS, RP0
    BSF     SSPCON2, SEN
    BCF     STATUS, RP0
I2C_START_W:
    BSF     STATUS, RP0
    BTFSC   SSPCON2, SEN
    GOTO    I2C_START_W
    BCF     STATUS, RP0
    RETURN

I2C_STOP_COND:
    BSF     STATUS, RP0
    BSF     SSPCON2, PEN
    BCF     STATUS, RP0
I2C_STOP_W:
    BSF     STATUS, RP0
    BTFSC   SSPCON2, PEN
    GOTO    I2C_STOP_W
    BCF     STATUS, RP0
    RETURN

I2C_RESTART_COND:
    BSF     STATUS, RP0
    BSF     SSPCON2, RSEN
    BCF     STATUS, RP0
I2C_RESTART_W:
    BSF     STATUS, RP0
    BTFSC   SSPCON2, RSEN
    GOTO    I2C_RESTART_W
    BCF     STATUS, RP0
    RETURN

I2C_WRITE_BYTE:
    BCF     STATUS, RP0
    MOVF    I2C_BUFFER, W
    MOVWF   SSPBUF
I2C_WRITE_W:
    BSF     STATUS, RP0
    BTFSC   SSPSTAT, R_W
    GOTO    I2C_WRITE_W
    BCF     STATUS, RP0
    RETURN

I2C_READ_BYTE:
    BSF     STATUS, RP0
    BSF     SSPCON2, RCEN
    BCF     STATUS, RP0
I2C_READ_W:
    BSF     STATUS, RP0
    BTFSC   SSPCON2, RCEN
    GOTO    I2C_READ_W
    BCF     STATUS, RP0
    MOVF    SSPBUF, W
    MOVWF   I2C_BUFFER
    RETURN

I2C_SEND_NACK:
    BSF     STATUS, RP0
    BSF     SSPCON2, ACKDT
    BSF     SSPCON2, ACKEN
    BCF     STATUS, RP0
I2C_NACK_W:
    BSF     STATUS, RP0
    BTFSC   SSPCON2, ACKEN
    GOTO    I2C_NACK_W
    BCF     STATUS, RP0
    RETURN

GET_SLAVE_BUTTONS:
    MOVLW   SLAVE_F0_ADDR
    MOVWF   I2C_ADDR
    CALL    READ_SLAVE_BTN
    MOVF    I2C_BUFFER, W
    MOVWF   F0_BUTTONS
    MOVLW   SLAVE_F1_ADDR
    MOVWF   I2C_ADDR
    CALL    READ_SLAVE_BTN
    MOVF    I2C_BUFFER, W
    MOVWF   F1_BUTTONS
    MOVLW   SLAVE_F2_ADDR
    MOVWF   I2C_ADDR
    CALL    READ_SLAVE_BTN
    MOVF    I2C_BUFFER, W
    MOVWF   F2_BUTTONS
    RETURN

READ_SLAVE_BTN:
    CALL    I2C_START_COND
    MOVF    I2C_ADDR, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVLW   CMD_GET_BUTTONS
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    CALL    I2C_RESTART_COND
    MOVF    I2C_ADDR, W
    IORLW   0x01
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    CALL    I2C_READ_BYTE
    CALL    I2C_SEND_NACK
    CALL    I2C_STOP_COND
    RETURN

SEND_LCD_UPDATES:
    MOVLW   SLAVE_F0_ADDR
    MOVWF   I2C_ADDR
    CALL    SEND_LCD_SLAVE
    MOVLW   SLAVE_F1_ADDR
    MOVWF   I2C_ADDR
    CALL    SEND_LCD_SLAVE
    MOVLW   SLAVE_F2_ADDR
    MOVWF   I2C_ADDR
    CALL    SEND_LCD_SLAVE
    RETURN

SEND_LCD_SLAVE:
    CALL    I2C_START_COND
    MOVF    I2C_ADDR, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVLW   CMD_UPDATE_LCD
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E1_STATE, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E1_CURRENT_FLOOR, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E1_DIRECTION, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E2_STATE, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E2_CURRENT_FLOOR, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E2_DIRECTION, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E3_STATE, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
    MOVF    E3_CURRENT_FLOOR, W
    MOVWF   I2C_BUFFER
    CALL    I2C_WRITE_BYTE
