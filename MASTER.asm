    RETURN

PROCESS_REQUESTS:
    CALL    PROCESS_F0_REQ
    CALL    PROCESS_F1_REQ
    CALL    PROCESS_F2_REQ
    RETURN

PROCESS_F0_REQ:
    BTFSS   F0_BUTTONS, 0
    GOTO    CHK_F0_STAFF
    BTFSC   FLOOR_SERVICED, 0
    GOTO    CHK_F0_STAFF
    CALL    ASSIGN_PUB_UP_F0
CHK_F0_STAFF:
    BTFSS   F0_BUTTONS, 4
    RETURN
    BTFSS   F0_BUTTONS, 2
    RETURN
    BSF     E3_REQUESTS_UP, 0
    RETURN

PROCESS_F1_REQ:
    BTFSS   F1_BUTTONS, 0
    GOTO    CHK_F1_DN
    BTFSC   FLOOR_SERVICED, 1
    GOTO    CHK_F1_DN
    CALL    ASSIGN_PUB_UP_F1
CHK_F1_DN:
    BTFSS   F1_BUTTONS, 1
    GOTO    CHK_F1_STAFF
    BTFSC   FLOOR_SERVICED, 1
    GOTO    CHK_F1_STAFF
    CALL    ASSIGN_PUB_DN_F1
CHK_F1_STAFF:
    BTFSS   F1_BUTTONS, 4
    RETURN
    BTFSC   F1_BUTTONS, 2
    BSF     E3_REQUESTS_UP, 1
    BTFSC   F1_BUTTONS, 3
    BSF     E3_REQUESTS_DN, 1
    RETURN

PROCESS_F2_REQ:
    BTFSS   F2_BUTTONS, 1
    GOTO    CHK_F2_STAFF
    BTFSC   FLOOR_SERVICED, 2
    GOTO    CHK_F2_STAFF
    CALL    ASSIGN_PUB_DN_F2
CHK_F2_STAFF:
    BTFSS   F2_BUTTONS, 4
    RETURN
    BTFSS   F2_BUTTONS, 3
    RETURN
    BSF     E3_REQUESTS_DN, 2
    RETURN

