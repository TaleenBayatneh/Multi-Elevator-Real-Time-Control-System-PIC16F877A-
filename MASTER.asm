MAIN_INIT:
    BSF     STATUS, RP0
    BCF     STATUS, RP1
    MOVLW   0x07
    MOVWF   TRISA
    CLRF    TRISB
    MOVLW   0x18
    MOVWF   TRISC
    CLRF    TRISD
    CLRF    TRISE
    MOVLW   0x06
    MOVWF   ADCON1
    MOVLW   0x09
    MOVWF   SSPADD
    BCF     STATUS, RP0
    MOVLW   0x28
    MOVWF   SSPCON
    CLRF    PORTA
    CLRF    PORTB
    CLRF    PORTC
    CLRF    PORTD
    CLRF    PORTE
    BSF     PORTB, 4
    BSF     PORTB, 5
    BSF     PORTB, 6
    CALL    INIT_ELEVATORS
    BSF     INTCON, GIE
    BSF     INTCON, PEIE

MAIN_LOOP:
    CALL    SCAN_ALL_SENSORS
    CALL    GET_SLAVE_BUTTONS
    CALL    PROCESS_REQUESTS
    CALL    UPDATE_ELEVATOR_1
    CALL    UPDATE_ELEVATOR_2
    CALL    UPDATE_ELEVATOR_3
    CALL    HANDLE_DOORS
    CALL    UPDATE_BUZZERS
    CALL    SEND_LCD_UPDATES
    CALL    DELAY_10MS
    GOTO    MAIN_LOOP

